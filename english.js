function parseVocabulary(text) {
  const lines = text.split('\n').map(line => line.trim()).filter(Boolean);
  const lessons = [];
  let currentLesson = null;

  const lessonRegex = /^Lesson\s+(\d+)/i;
  const wordRegex = /^\d+\s+(\w+)\s*(\[.*?\])?\s*([a-z.]+)?\s*(.+)?$/i;

  for (const line of lines) {
    const lessonMatch = line.match(lessonRegex);
    if (lessonMatch) {
      currentLesson = {
        lesson: `Lesson ${lessonMatch[1]}`,
        words: []
      };
      lessons.push(currentLesson);
      continue;
    }

    const wordMatch = line.match(wordRegex);
    if (wordMatch && currentLesson) {
      const [_, word, phonetic = '', type = '', meaning = ''] = wordMatch;
      currentLesson.words.push({
        word,
        phonetic: phonetic,
        type: type.trim(),
        meaning: meaning.trim(),
        id: word
      });
    }
  }

  return lessons;
}
